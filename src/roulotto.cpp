bool ROULOTTO::isGreen(int number) {
    // Handle special cases for 0, 00, and 000
    if (number == 0 || number == 37 || number == 38) {
        return true; // Neither red nor black
    }
    return false;
}

bool ROULOTTO::isRed(int number) {
    // Handle special cases for 0, 00, and 000
    if (isGreen(number)) {
        return false; // Neither red nor black
    }

    // Check if the number is in the red set
    return std::find(std::begin(ROULOTTO::redNumbers), std::end(ROULOTTO::redNumbers), number) != std::end(ROULOTTO::redNumbers);
}

std::string ROULOTTO::getColorOfNumber(int number) {
    if (isGreen(number)) {
        return "Grn";
    } else if (isRed(number)) {
        return "Red";
    } else {
        return "Blk";
    }
}

std::string ROULOTTO::betToString(int insideBet, int outsideBet) {
    Serial.printf("[ROULOTTO - B2S] Inside Bet: %d, Outside Bet: %d\n", insideBet, outsideBet);
    if (insideBet != -1) {
        return "Inside " + std::to_string(insideBet);
    }
    switch (outsideBet) {
        case 1: return "Outside Red";
        case 2: return "Outside Black";
        case 3: return "Outside Even";
        case 4: return "Outside Odd";
        case 5: return "Outside High";
        case 6: return "Outside Low";
        default: return "Unknown";
    }
}

